"""
Модуль конфигурации приложения MyDay.

Этот модуль отвечает за управление конфигурацией окружения, включая:

- Учётные данные для API погоды (OpenWeatherMap)
- Настройки города/локации по умолчанию
- Сообщения и ответы, связанные с погодой

Конфигурация использует переменные окружения, загружаемые из файла `.env`.

Атрибуты:
    WEATHER_MESSAGES (dict): Словарь мотивирующих сообщений о погоде,
        организованных по типам погоды (Clear, Clouds, Rain, Snow и т.д.)
    WEATHER_URL (str): Endpoint API OpenWeatherMap

Пример:
    Получение API ключа для погоды::

        api_key = get_weather_api_key()
        city = get_city()
"""

from dotenv import load_dotenv
import os


def get_weather_api_key():
    """
    Получить API ключ OpenWeatherMap из переменных окружения.

    Загружает переменные окружения из файла `.env` и возвращает
    API ключ, установленный в переменной окружения WEATHER_API_KEY.

    Возвращает:
        str: API ключ для OpenWeatherMap API, или пустая строка если не найден.
    """
    load_dotenv(override=True)
    return os.getenv("WEATHER_API_KEY", "")


def get_city():
    """
    Получить город по умолчанию из переменных окружения.

    Загружает переменные окружения из файла `.env` и возвращает
    название города для запросов к API погоды. По умолчанию возвращает
    'Saint Petersburg' если значение не настроено.

    Возвращает:
        str: Название города для запросов о погоде.
    """
    load_dotenv(override=True)
    return os.getenv("CITY", "Saint Petersburg")


