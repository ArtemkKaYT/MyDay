"""
Модуль точки входа приложения MyDay.

MyDay - это персональный помощник для ежедневного управления, который предоставляет:

- Информацию о погоде в реальном времени
- Управление расписанием и событиями
- Создание и управление заметками
- Ежедневную сводку с приветствием

Приложение использует PyQt6 для создания пользовательского интерфейса
и предоставляет чистую, отзывчивую панель управления с различными виджетами.

Пример:
    Запуск приложения::

        python main.py
"""

import sys
from PyQt6.QtWidgets import QApplication

from ui.main_window import MainWindow


def main():
    """
    Инициализация и запуск приложения MyDay.

    Создаёт экземпляр QApplication, инициализирует главное окно
    и запускает цикл обработки событий приложения.

    Возвращает:
        None

    Исключения:
        SystemExit: При закрытии приложения.
    """
    app = QApplication(sys.argv)

    window = MainWindow()
    window.show()

    sys.exit(app.exec())


if __name__ == "__main__":
    main()
